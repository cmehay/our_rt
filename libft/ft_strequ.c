#include "includes/libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s1[i] == s2[j] && s1[i] != '\0' && s2[j] != '\0')
	{
		i++;
		j++;
	}
	if (s1[i] != s2[j])
		return (0);
	else
		return (1);
}